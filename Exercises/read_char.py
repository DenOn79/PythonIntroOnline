
symbol = input('Please input one of these symbols: A, B or C\n')
if symbol == 'A' or symbol == 'a':
    print('\"A\" stands for \"America\"')
else:
    if symbol == 'B' or symbol == 'b':
        print('\"B\" stands for \"Britain\"')
    else:
        if symbol == 'C' or symbol == 'c':
            print('\"C\" stands for \"Canada\"')


